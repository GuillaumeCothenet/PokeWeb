					<p class="lead">La prochaine mise à jour du jeu Pokemon Go promet déjà de faciliter la capture des Pokemon rares, et c’est une très bonne nouvelle.</p>
					<p>Annoncée il y a peu par <a href="http://nianticlabs.com/" target="_blank">Niantic</a>, <strong>la prochaine mise à jour de Pokemon Go pourrait bien transformer votre expérience de jeu</strong>, et notamment en matière de capture de Pokemon. Car si la nouvelle mise à jour proposera un <a href="http://www.melty.fr/pokemon-go-nouveaux-changements-pour-les-combats-en-arene-a561716.html" target="_blank">changement pour les combats en arène dans Pokemon GO</a>, d'autres aspects du jeu seront également modifiés, pour le plus grand bonheur de toute la rédac du Coca-Cola Gaming Stories. Comme l'expliquaient les développeurs du jeu, dans quelques semaines, chaque Pokemon capturé vous permettra de remplir une jauge, selon son type. Au bout d'un certain nombre de créatures du même type attrapé, <strong>vous débloquerez un bonus de jeu, qui facilitera fortement la recherche et la capture des prochains Pokemon de ce type</strong>. <em>"Ces bonus vous offriront de meilleures chances de capturer les Pokemon d'un certain type. Par exemple, explique Niantic, plus vous capturez de Pokemon feu, plus votre bonus de capture augmentera sur les Pokemon comme Salamèche, Goupix ou encore Ponyta."</em></p>
					<p>Avec cette nouvelle fonctionnalité, <strong>les dresseurs pourront ainsi baser leur stratégie sur la capture d'un type de Pokemon spécifique</strong>. À noter que si le Pokemon capturé possède plusieurs type, les deux seront pris en compte. Par exemple, pour un Roucool qui est de type vol et normal, votre bonus sera pris en compte pour le type normal, mais aussi pour le type vol. Cerise sur le gâteau, Niantic a précisé que ces bonus de jeu pourraient se révéler particulièrement utiles pour dénicher des <a href="http://www.melty.fr/pokemon-go-comment-attraper-des-pokemon-rares-a562839.html" target="_blank">Pokemon rares, qui sont souvent une vraie galère à trouver dans Pokemon Go</a>. <em>Que pensez-vous de cette nouvelle fonctionnalité de jeu ?</em></p>
